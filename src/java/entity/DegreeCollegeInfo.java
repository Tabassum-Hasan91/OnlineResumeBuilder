public class DegreeCollegeInfo 
{
    String degClgName;
    String degCourse;
    String degCity;
    String degUniversity;
    String degYear;

    public String getDegClgName() {
        return degClgName;
    }

    public void setDegClgName(String degClgName) {
        this.degClgName = degClgName;
    }

    public String getDegCourse() {
        return degCourse;
    }

    public void setDegCourse(String degCourse) {
        this.degCourse = degCourse;
    }

    public String getDegCity() {
        return degCity;
    }

    public void setDegCity(String degCity) {
        this.degCity = degCity;
    }

    public String getDegUniversity() {
        return degUniversity;
    }

    public void setDegUniversity(String degUniversity) {
        this.degUniversity = degUniversity;
    }

    public String getDegYear() {
        return degYear;
    }

    public void setDegYear(String degYear) {
        this.degYear = degYear;
    }
    
    
    
}
