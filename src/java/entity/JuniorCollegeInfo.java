public class JuniorCollegeInfo 
{
    String jrClgName;
    String jrCourse;
    String jrCity;
    String jrUniversity;
    String jrYear;

    public String getJrClgName() {
        return jrClgName;
    }

    public void setJrClgName(String jrClgName) {
        this.jrClgName = jrClgName;
    }

    public String getJrCourse() {
        return jrCourse;
    }

    public void setJrCourse(String jrCourse) {
        this.jrCourse = jrCourse;
    }

    public String getJrCity() {
        return jrCity;
    }

    public void setJrCity(String jrCity) {
        this.jrCity = jrCity;
    }

    public String getJrUniversity() {
        return jrUniversity;
    }

    public void setJrUniversity(String jrUniversity) {
        this.jrUniversity = jrUniversity;
    }

    public String getJrYear() {
        return jrYear;
    }

    public void setJrYear(String jrYear) {
        this.jrYear = jrYear;
    }
        
}
